var redLink = document.getElementById("red");
var greenLink = document.getElementById("green");
var blueLink = document.getElementById("blue");
var yellowLink = document.getElementById("yellow");

redLink.onmouseover = function () {
    document.body.style.backgroundColor = "red";
};

greenLink.onmouseover = function () {
    document.body.style.backgroundColor = "green";
};

blueLink.onmouseover = function () {
    document.body.style.backgroundColor = "blue";
};

yellowLink.onmouseover = function () {
    document.body.style.backgroundColor = "yellow";
};